# Функція що приймає змінне число аргументів та повертає максимальне значення
def max (*a): # Всі передані в цю ф-ю параметри будуть збиратися в один кортеж, на що вказує зірочка перед a
    max_val = a[0]
    for val in a[1:]:
        if val > max_val:
            max_val = val
    return max_val
print(max(-15,52,-147,85,-3,-6,-21,154,-125,14))
